package ym.formbean;

public class HospitalFormBean {
	private int areaId;
	private String hospitalName;
	private int cancerCategoryId;
	
	public int getAreaId() {
		return areaId;
	}
	public void setAreaId(int areaId) {
		this.areaId = areaId;
	}
	public String getHospitalName() {
		return hospitalName;
	}
	public void setHospitalName(String hospitalName) {
		this.hospitalName = hospitalName;
	}
	public int getCancerCategoryId() {
		return cancerCategoryId;
	}
	public void setCancerCategoryId(int cancerCategoryId) {
		this.cancerCategoryId = cancerCategoryId;
	}
	

}
